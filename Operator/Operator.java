
public class Operator {

	public static void main(String[] args) {
		
		int i = 29;
		int a = 77;
		int b = 33;
		
		i*=a+b;
		System.out.println(i);
		
		i +=a+b;
		System.out.println(i);
		
		i-=a+b;
		System.out.println(i);
		
		i/=a+b;
		System.out.println(i);
		
		i%=a+b;
		System.out.println(i);

	}

}
